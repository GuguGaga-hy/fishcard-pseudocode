package cards;

import gamecore.*;

/**
 * 被动卡牌：隐匿
 * 受到伤害时可使用，给予等同于存活玩家数量的不可选中层数（延迟生效）
 */
public class 隐匿 extends 卡牌 {
    隐匿() {
        名称 = "隐匿";
        描述 = "受到伤害时，获得等同于存活玩家数量的不可选中层数（隐身），本次伤害正常结算";
        是否无消耗 = true;   // 被动牌不占用出牌机会
        是否被动 = true;
    }

    @Override
    public void 使用效果(玩家 出牌者, 玩家 目标) {
        int 存活数量 = 出牌者.所属游戏.获取存活玩家().size();
        int 层数 = Math.min(10, 存活数量);
        词条.延迟施加不可选中(目标, 层数, this);
    }
}