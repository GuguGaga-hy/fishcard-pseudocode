package cards;

import gamecore.*;

/**
 * 群攻卡牌：地裂术
 * 对除自己外的所有存活玩家造成1点物理伤害
 */
public class 地裂术 extends 卡牌 {
    地裂术() {
        名称 = "地裂术";
        描述 = "对除自己以外的所有玩家造成1点物理伤害";
        是否无消耗 = false;
        是否被动 = false;
    }

    @Override
    public void 使用效果(玩家 出牌者, 玩家 目标) {
        // 获取除出牌者以外的所有存活玩家
        for (玩家 p : 出牌者.所属游戏.获取存活玩家()) {
            if (p != 出牌者) {
                出牌者.所属游戏.造成伤害(出牌者, p, 伤害类型.物理伤害, 1, this);
            }
        }
        // 注：这里的参数“目标”未使用，因为群攻不需要单个目标
    }
}