package cards;

import gamecore.*;

/**
 * 卡牌：天堂之门
 * 效果：立即结束行动窗口，接下来两次自己的行动窗口进入脱战状态（状态完全冻结，无法做出动作）
 */
public class 天堂之门 extends 卡牌 {
    天堂之门() {
        名称 = "天堂之门";
        描述 = "立即结束行动窗口，接下来两次自己的行动窗口脱战（无法行动、无法被选中）";
        是否无消耗 = false;
        是否被动 = false;
    }

    @Override
    public void 使用效果(玩家 出牌者, 玩家 目标) {
        // 立即结束当前行动窗口
        出牌者.强制结束行动();
        // 设置接下来两次自己的行动窗口脱战
        出牌者.设置脱战计数(2);
    }
}